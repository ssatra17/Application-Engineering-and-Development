    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        int row = tblAirlines.getSelectedRow();
        if(row>=0) {
             int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete??","Warning",selectionButton);
            if(selectionResult == JOptionPane.YES_OPTION){
                Airlines airlines = (Airlines)tblAirlines.getValueAt(row, 0);
        airlineDirectory.removeAirlines(airlines);
        populate();
            }}
            else{
                
            
            JOptionPane.showMessageDialog(null, "Please select a row from the table first");
            return;
        }
        
                                   
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int row = tblAirlines.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Airlines a = (Airlines)tblAirlines.getValueAt(row, 0);
        ViewAirlineSchedule vas = new ViewAirlineSchedule(bottomPanel, a,f);
        bottomPanel.add("ViewAirlineSchedule", vas);
        CardLayout layout = (CardLayout)bottomPanel.getLayout();
        layout.next(bottomPanel);
                                              
    }//GEN-LAST:event_btnViewActionPerformed


