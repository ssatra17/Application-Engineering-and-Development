    public void populate(){
       
        DefaultTableModel dtm = (DefaultTableModel)tblAirlines.getModel();
        dtm.setRowCount(0);
        for(Airlines a : airlineDirectory.getAirlinedirectory()){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0]=a;
            row[1]=a.getOrigin();
           
             dtm.addRow(row);
           
 
        }
        int rowCount = tblAirlines.getRowCount();
    }
